package com.example.stl.json3;

public class ConsumerActivity {
    private int summary;
    private String make;
    private String model;
    private int zipcode;
    private int SRPCount;
    private int VDPCount;

    public ConsumerActivity() {}

    public int getSummary() {
        return summary;
    }

    public void setSummary(int summary) {
        this.summary = summary;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public int getZipcode() {
        return zipcode;
    }

    public void setZipcode(int zipcode) {
        this.zipcode = zipcode;
    }

    public int getSRPCount() {
        return SRPCount;
    }

    public void setSRPCount(int SRPCount) {
        this.SRPCount = SRPCount;
    }

    public int getVDPCount() {
        return VDPCount;
    }

    public void setVDPCount(int VDPCount) {
        this.VDPCount = VDPCount;
    }
}
